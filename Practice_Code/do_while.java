public class do_while {
    public static void main(String[] args) {

        //while loop
        int a = 0;
        while(a<5){
            System.out.println(a);
            a++;
        }
        
        //do while loop
        int b = 0;
        do{
            System.out.println(b);
            b++;
        }while(b<5);
    }
}
